create table public.pages (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  name text not null,
  "order" jsonb not null default '[]'::jsonb,
  user_id uuid not null default auth.uid (),
  active boolean not null default false,
  "primary" text null,
  secondary text null,
  tertiary text null,
  quaternary text null,
  meta_title text null,
  meta_description text null,
  meta_img text null,
  vcard text null,
  bg_color text not null default '#ffffff'::text,
  sms_number text null,
  sms_message text null,
  email_address text null,
  email_content text null,
  foreground_color text not null default '#ffffff'::text,
  font_name text null default 'Inter'::text,
  font_color text not null default '#000000'::text,
  bg_image text null,
  constraint pages_pkey primary key (id),
  constraint pages_name_key unique (name),
  constraint pages_user_id_fkey foreign KEY (user_id) references profiles (id)
) TABLESPACE pg_default;